     	<h3 style="margin-top: 50px;margin-left: 289px;">Những ưu điểm khi sống trong Ký túc xá</h3>
     	<p>Học sinh, sinh viên nội trú tại Ký túc xá sẽ có một môi trường xã hội tích cực để học tập và rèn luyện, từ đó có điều kiện xây dựng cơ sở vững chắc hơn cho tương lai. Khi sống trong môi trường Ký túc xá, học sinh, sinh viên nội trú sẽ có những kỷ niệm sâu sắc và những người bạn suốt đời. Sống trong khuôn viên Ký túc xá, học sinh, sinh viên sẽ có điều kiện ở gần các giảng đường, phòng thí nghiệm, thư viện,... của Trường. Ngoài ra, khi sống ở Ký túc xá, học sinh, sinh viên sẽ sống và học tập trong một môi trường xanh, sạch, đẹp và an toàn.</p>
        <p>
        Học sinh, sinh viên cũng có điều kiện để sử dụng các dịch vụ khác: dịch vụ giao thông (Ký túc xá nằm gần bến xe Mỹ Đình, ga tàu điện trên cao, bến xe bus); dịch vụ y tế (Ký túc xá nằm gần các bệnh viện lớn, có uy tín như: Bệnh viện Bộ Công an 19/8, Bệnh viện E,...); các tiện ích xã hội khác (Ký túc xá nằm gần công viên Cầu Giấy, Công viên Nghĩa Đô, Bảo tàng Dân Tộc học,…). Đây sẽ là các điều kiện hết sức lý tưởng cho sinh hoạt và học tập của học sinh, sinh viên.</p>
