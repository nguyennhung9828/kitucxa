      <p>- Quản lý sinh viên ngoài giờ lên lớp:</p>
      <p>- Bảo đảm an ninh chính trị, trật tự an toàn, phòng chống cháy nổ và vệ sinh môi trường</p>
      <p>- Quản lý, tổ chức các dịch vụ phục vụ sinh viên</p>
